const jwt = require('jsonwebtoken');
const { User } = require('../models');

// Middleware to authenticate users with JWT
exports.authenticate = async (req, res, next) => {
  try {
    // Get token from header
    const token = req.header('x-auth-token');
    
    // Check if no token
    if (!token) {
      return res.status(401).json({ message: 'No token, authorization denied' });
    }
    
    // Verify token
    const decoded = jwt.verify(token, process.env.JWT_SECRET);
    
    // Add user from payload
    const user = await User.findByPk(decoded.id);
    
    if (!user) {
      return res.status(401).json({ message: 'User not found' });
    }
    
    if (!user.isActive) {
      return res.status(401).json({ message: 'User account is inactive' });
    }
    
    req.user = user;
    next();
  } catch (err) {
    res.status(401).json({ message: 'Token is not valid' });
  }
};

// Middleware to check if user is a stylist
exports.isStylist = (req, res, next) => {
  if (req.user && req.user.role === 'stylist') {
    next();
  } else {
    res.status(403).json({ message: 'Access denied. Stylist role required.' });
  }
};

